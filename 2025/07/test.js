const { countSplits } = require('./index');

describe('countSplits', () => {
    it('Example 1', () => {
        expect(countSplits([
            '.......S.......',
            '...............',
            '.......^.......',
            '...............',
            '......^.^......',
            '...............',
            '.....^.^.^.....',
            '...............',
            '....^.^...^....',
            '...............',
            '...^.^...^.^...',
            '...............',
            '..^...^.....^..',
            '...............',
            '.^.^.^.^.^...^.',
            '...............',
        ])).toEqual(21);
    });
});
